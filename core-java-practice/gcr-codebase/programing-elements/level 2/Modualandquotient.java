import java.util.*;

public class Modualandquotient {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int m = sc.nextInt();
        int div = n / m;
        int mod = n % m;
        System.out.println("the quotient is" + div + "the rem is" + mod + "two number is" + n + " "  + m );
        
    }
    
}
